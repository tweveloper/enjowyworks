class ParamToVO
{
    private $deviceID;

    //##################################################################################################################
    //##
    //## >> Override
    //##
    //##################################################################################################################

    public function __construct($deviceID = null)
    {
        if(!empty($deviceID))$this->deviceID = $deviceID;
    }

    public function __set($name, $value)
    {
        $this->{$name} = $value;
    }

    public function __get($name)
    {
        return $this->{$name};
    }

    public function __toString()
    {
        $res = "";
        $res .= "=============== 디바이스 ID ===============================<br>";
        $res .= "결과코드              : ".$this->deviceID."<br>";
        return $res;
    }
}